# Assignment operators
a = 11
a += 5
a_type = type(a)
print(f"The value of a is {a} and the type of a is {a_type}")

# Comparison operators
a = 11
b = 5
is_a_less_than_b = a < b
comparison_type = type(is_a_less_than_b)
print(f"The result of {a} < {b} is {is_a_less_than_b} and the type of the result is {comparison_type}")
